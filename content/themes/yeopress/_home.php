                        <h2>About</h2>

                        <div class="text-col-2">
                            <p>In New York City, the income gap between rich and poor is almost twice that of the rest of the country. The average rent for a studio apartment in Bushwick has risen 27% in two years. What happens to those who can’t keep up? Landlords are illegally harassing and evicting tenants all over Brooklyn. Meanwhile, wages stay the same and some employers even steal the earnings of their workers. The results are poverty, displacement, and the erosion of vital communities.</p>

                            <p>What can we do? There are cases when turning to the court system is not enough. There is a history of the abuses of landlords and employers being stopped by the direct action of regular people who have each others’ backs. Groups like these are called Solidarity Networks, and they exist all over the country. They take action in the form of phone blasts, flyering, picketing, and more to stand up for their neighbors and demand justice. Our aim is to create and sustain a network like this here in Brooklyn.</p>

                            <p>We want to build bonds now with our neighbors who are struggling and do what we can to stand up to exploitation. If you are facing exploitation and think that connecting with a solidarity network may help, please reach out to us. We can sit down together and figure out how to address the problem. As we win victories, those we help can join us and stand up for others. This is how our power grows.</p>
