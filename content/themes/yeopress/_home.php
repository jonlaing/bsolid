                    <h2>FAQ</h2>

                    <div class="col-5 prepend-1">
                        <dl>
                            <dt>Is this a charity service?</dt>
                            <dd><strong>No.</strong> We can&apos;t stress that enough. BSolid has no interest in charity, our goal is <em>solidarity</em>. A simple way to illustrate the difference is the old parable about giving a man a fish (charity) versus teaching a man to fish (solidarity&hellip; sort of). One of BSolid&apos;s core beliefs is that people have the power to direct their lives for the better without having to rely on charity. The reason this very often does not happen is that there are forces in this world (capitalism) that isolate us and condition us to be helpless. That&apos;s where BSolid comes in. Our mission is not to provide a service, but to break down the social walls that isolate us and make us powerless.</dd>

                            <dt>Are you an anti-gentrification group?</dt>
                            <dd>Yes and no. Our goal is to empower the working class against the forces that exploit them. In Brooklyn, this often means fighting gentrification, and we do definitely take a special interest in gentrification. However, we also take on labor issues, and are willing to help with any issue of exploitation in Brooklyn.</dd>

                            <dt>How do you achieve victories?</dt>
                            <dd>We use what&apos;s called &ldquo;escalating campaigns&rdquo;. This means we start small with a demand letter. The demand letter is crafted by the person in need of solidarity with the help of organizers. From there we have a plethora of escalation tactics including but not limited to phone blasts, picketing, and disrupting business. The reason we operate this way is the core belief that the needs of the working class and the priorities of owning/ruling class are antagonistic of one another. That means that it&apos;s often futile to appeal to the better conscience of those in charge. The only thing they understand is money. So grow our power, we hit them where it hurts. In the wallet. Eventually, the owners will be left with the choice of losing their business or capitulating to us.</dd>

                            <dt>Isn&apos;t that just bullying business owners?</dt>
                            <dd>We&apos;re going to answer that question by asking another: If a kid on the playground is constantly getting the crap beaten out of them by a bigger, stronger kid every day, would the first kid be bullying if one day he turned around and knocked his bully out?</dd>
                        </dl>
                    </div>
                    <div class="col-5">
                        <dl>
                            <dt>What about the courts and lawyers?</dt>
                            <dd>Another core belief of BSolid is that laws are not written to protect the people, but rather to keep power structures in place. As such, courts often favor those with power. In this case that means your employer and your landlord. By using courts, we are participating in a system we feel is unjust. Not only that, but courts are prohibitively expensive, and we&apos;d probably lose anyway. In fact, many of the cases we take on have already been to court with no resolution. <b>In short:</b> if you wish to build your power, you can&apos;t rely on the systems that have kept you down all this time.</dd>

                            <dt>Do you do any work in Queens</dt>
                            <dd>BSolid doesn&apos;t actually believe in the legitimacy of borders, so if you live in Queens, or the Bronx or wherever, and are in need of solidarity, give us a call. We do try to focus on Brooklyn only because that&apos;s where most of us live.</dd>

                            <dt>When do you meet?</dt>
                            <dd>It&apos;s a little up in the air at the moment, to be honest. We try to meet <em>at least</em> once a week, generally in the evening. The best way to know when the next we&apos;re having our next meeting is to <a href="https://www.facebook.com/bklynsolidaritynetwork" target="_blank">follow us on Facebook.</a> You can also shoot us an email at <a href="mailto:bklynsolidaritynetwork@gmail.com">bklynsolidaritynetwork@gmail.com</a></dd>

                            <dt>Where do you meet?</dt>
                            <dd>We usually meet at <a href="http://thebasebk.org" target="_blank">The Base</a> in Bushwick, Brooklyn.</dd>

                            <dt>How do I get involved?</dt>
                            <dd>The best way is to <a href="http://goo.gl/rLSjyU" target="_blank">Sign up for our Call List</a>. Once you&apos;re on this list, we will invite you to actions to give your solidarity. If you&apos;re in need of solidarity, just give us a call at <a href="tel:+13155576543">(315) 55-SOLID</a>.</dd>
                        </dl>
