                        <h2>Mission</h2>

                        <p>What is a Solidarity network? People having each others’ backs. A group of people willing to take direct action against the exploitation of members of our community. This can take the form of phone blasts, flyering, picketing, and other measures to stand up to the abuse of landlords, bosses, and other exploitative forces!</p>

